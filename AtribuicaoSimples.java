public class AtribuicaoSimples {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("O valor inicial de x = " + x);

        x += 5;
        System.out.println("O valor de x + 5 = " + x);

        x -= 2;
        System.out.println("O valor de x - 2 = " + x);

        x *= 3;
        System.out.println("O valor de x * 3 = " + x);

        x /= 3;
        System.out.println("O valor de x / 3 = " + x);

        x %= 5;
        System.out.println("O valor de x % 5 = " + x );
    }
}
